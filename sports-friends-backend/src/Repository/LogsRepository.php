    public function addLogs(String $name, String $surname){
        $entityManager = $this->getEntityManager();
        $date = new DateTime();
        $log = new Logs();
        $log->setName($name);
        $log->setSurname($surname);
        $date->format('Y-m-d');
        $log->setDatetime($date);
        try {
            $entityManager->persist($log);
            $entityManager->flush();
        } catch (ORMException $e) {
        }
    }

